package com.openweather.weather.dto;

public class Coordinates {
	Float lon;
	Float lat;

	public float getLon() {
		return lon;
	}

	public void setLon(Float lon) {
		this.lon = lon;
	}

	public float getLat() {
		return lat;
	}

	public void setLat(Float lat) {
		this.lat = lat;
	}
}
